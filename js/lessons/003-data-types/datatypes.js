/*
Data Types
    - string
    - number
    - array (object)
    - Object -> Like dict in python
    - boolean
    - undefined
    - null -> Object
*/
console.log(typeof "Abdelrahman Elsayed"); // string
console.log(typeof 5000); // number
console.log(typeof 50.1); // number
console.log(typeof [21, 53]); // object [in any language array]
console.log(typeof ["abdelrahman", "Elsayed"]); // object [in any language array]
console.log(
  typeof { name: "Abdelrahman", age: 20, title: "ERPNext Developer" }
); // object like dict in python

console.log(typeof false);
console.log(typeof undefined);
console.log(typeof null);
