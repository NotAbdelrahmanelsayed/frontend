console.log(10 == "10"); // true -> == compare value only
console.log(-1 == 1); // false

console.log(10 != "10"); // false

console.log(10 === "10"); // false -> === Means identical
console.log(10 !== "10"); // true -> !== not identical but same value
console.log(10 !== 10); // true -> !== identical and same value

console.log(10 > 12);
console.log(10 >= 10); // true :)

